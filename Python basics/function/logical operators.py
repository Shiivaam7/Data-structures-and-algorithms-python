c = True
d = False

#logical operators(	Returns True if both statements are true)
print(c and c)
print(c and d)
print(d and c)
print(c and d)

#or(Returns True if one of the statements is true)
print(True or False)
print(True or False)
print(False or True)
print(False or False)

#not operators(Reverse the result, returns False if the result is true)
print("Not opertors")
print(not(True))
print(not(False))
